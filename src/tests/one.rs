#[test]
fn run() {
    let list = vec![
        1000, 2000, 3000, 0, 4000, 0, 5000, 6000, 0, 7000, 8000, 9000, 0, 10000,
    ];

    let list2 = vec![
        1000, 2000, 3000, 0, 24000, 0, 5000, 6000, 0, 7000, 8000, 9000, 0, 30000,
    ];

    let list3 = vec![
        1000, 12000, 13000, 0, 24000, 0, 5000, 6000, 0, 7000, 8000, 9000, 0
    ];

    assert_eq!(crate::one::one(list), 24000);
    assert_eq!(crate::one::one(list2), 30000);
    assert_eq!(crate::one::one(list3), 26000);
}
